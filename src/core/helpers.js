// ПОМОЩНИЦИТЕ.
//
// Всичко, което пипа кода на ученика, преди проверката да го види.
//
// ДВЕ ГЛЕДНИ ТОЧКИ КЪМ ЕДИН КОД — и двете ти трябват.
//
//   1. DOM-ът — какво СТАНА.
//      Браузърът поправя грешките мълчаливо. <h1> в <head> го мести в <body>.
//      Незатворен таг го затваря. Питаш: „вижда ли се", „в <nav> ли е".
//
//   2. Суровият низ — какво НАПИСА.
//      DOM-ът вече е скрил грешката. Тук тя още стои.
//      Питаш: „написа ли <h1> вътре в <head>".
//
// Урок 41 е точно за това: „Кодът РАБОТИ. Но е счупен."
// За да го хванеш, трябва да видиш и двете.
// ============================================

// Коментарите не са код.
// Без това `<!-- <h1>Заглавие</h1> -->` минава проверката „има ли h1".
export const removeHtmlComments = (s) => (s ?? '').replace(/<!--[\s\S]*?-->/g, '');

// Меко сравнение: интервали и главни букви не се наказват.
export const norm = (s) => (s ?? '').toLowerCase().replace(/\s+/g, ' ').trim();

// ── ИСТИНСКИЯТ DOM ──
// DOMParser е вграден в браузъра. Нула килобайта, нула зависимости.
// Той Е браузърът — значи вижда точно каквото вижда ученикът.
export const parse = (s) => new DOMParser().parseFromString(s ?? '', 'text/html');

// ── СУРОВИЯТ ТЕКСТ ──
// Какво стои буквално вътре в head/body, ПРЕДИ браузърът да го е пипал.
// Няма таг → празно. Не пада на целия код — иначе проверката лъже.
export const rawHead = (s) => (s ?? '').match(/<head[^>]*>([\s\S]*?)<\/head>/i)?.[1] ?? '';
export const rawBody = (s) => (s ?? '').match(/<body[^>]*>([\s\S]*?)<\/body>/i)?.[1] ?? '';

// Видимият текст — това, което човек чете на екрана.
export function visibleText(html) {
  const doc = parse(html);
  return norm(doc.body?.textContent ?? '');
}

// ── БАЛАНСЪТ ──
// Затворени ли са таговете, и то в правилен ред?
// Стек: отворен таг влиза, затварящ трябва да срещне същия отгоре.
//
// ⚠ Код БЕЗ тагове не е „балансиран" — той е празен.
// Иначе `asdasdasd` минава проверката „всичко затворено" и това е фалшива похвала.
//
// DOM-ът тук НЕ помага: той затваря всичко сам. Затова е низова проверка.
export function balanced(html) {
  const VOID = new Set(['br', 'img', 'hr', 'input', 'meta', 'link', 'source', 'area', 'base', 'col', 'embed', 'track', 'wbr']);
  const code = removeHtmlComments(html);
  const stack = [];
  const re = /<\s*(\/?)\s*([a-zA-Z][a-zA-Z0-9]*)[^>]*?(\/?)\s*>/g;
  let m;
  let seen = 0;

  while ((m = re.exec(code))) {
    const closing = m[1] === '/';
    const name = m[2].toLowerCase();
    const selfClosed = m[3] === '/';
    seen++;

    if (VOID.has(name) || selfClosed) continue;

    if (!closing) {
      stack.push(name);
    } else {
      if (stack.length === 0) return false;      // затваря нещо, което не е отворено
      if (stack.pop() !== name) return false;    // затваря в грешен ред
    }
  }

  if (seen === 0) return false;                   // няма нито един таг
  return stack.length === 0;                      // нищо не е останало отворено
}